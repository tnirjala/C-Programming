    private void Delete_btnumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btnumActionPerformed
        String record, username;
        managingUser hi = new managingUser();


        hi.setUsername(Usernameum.getText());

        File db = new File("Login.txt");
        File tempdb = new File("Logintmp.txt");

        BufferedReader br;
        try {
            br = new BufferedReader(new FileReader(db));
            BufferedWriter bw = new BufferedWriter(new FileWriter(tempdb));

            username = hi.getUsername();

            while ((record = br.readLine()) != null) {
                if (record.contains(username)) {
                    continue;
                }
                bw.write(record);
                bw.flush();
                bw.newLine();
            }
            br.close();
            bw.close();
            db.delete();

            tempdb.renameTo(db);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Usermanagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Usermanagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel tbl = (DefaultTableModel) Usermanagement_table.getModel();
        if (Usermanagement_table.getSelectedRowCount() == 1) {
            tbl.removeRow(Usermanagement_table.getSelectedRow());
            JOptionPane.showMessageDialog(this, "The Data Has been Deleted Sucessfully.");

        } else {
            if (Usermanagement_table.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Empty Table Found");
            } else {
                JOptionPane.showMessageDialog(this, "Select the Row to Delete the record");
            }
        }
    }//GEN-LAST:event_Delete_btnumActionPerformed

