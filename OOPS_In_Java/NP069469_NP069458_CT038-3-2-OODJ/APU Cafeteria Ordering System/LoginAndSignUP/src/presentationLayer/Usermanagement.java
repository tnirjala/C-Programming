    private void Usermanagement_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Usermanagement_tableMouseClicked
        // To show the data after clickling the Table
        int i = Usermanagement_table.getSelectedRow();
        TableModel model = Usermanagement_table.getModel();
        User_idum.setText(model.getValueAt(i, 0).toString()); //The data is set to
        Usernameum.setText(model.getValueAt(i, 1).toString());
        Passwordum.setText(model.getValueAt(i, 2).toString());
        Phonenoum.setText(model.getValueAt(i, 3).toString());
        Emailum.setText(model.getValueAt(i, 4).toString());
        String combox = model.getValueAt(i, 5).toString();
        if (combox.equals("Manager")) {
            Combousermanagement.setSelectedIndex(0);
        } else if (combox.equals("Customer")) {
            Combousermanagement.setSelectedIndex(1);
        }

    }//GEN-LAST:event_Usermanagement_tableMouseClicked

