    private void Update_btnumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btnumActionPerformed
        // TODO add your handling code here:
        managingUser hi = new managingUser();
        managingUserdao daoms = new managingUserdaoimpl();

        hi.setUsername(Usernameum.getText());
        hi.setPassword(Passwordum.getText());
        hi.setEmail(Emailum.getText());
        hi.setUserid(User_idum.getText());
        hi.setPhone(Integer.parseInt(Phonenoum.getText()));
        hi.setDesignation(Combousermanagement.getSelectedItem().toString());

        if (User_idum.getText().equals("") || User_idum.getText().equals("") || Usernameum.getText().equals("") || Passwordum.getText().equals("") || Phonenoum.getText().equals("") || Emailum.getText().equals("") || Combousermanagement.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Fill all the field to process.");
        } else {
            daoms.Updatebtnum(hi);

            int i = Usermanagement_table.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) Usermanagement_table.getModel();
            if (i >= 0) {
                model.setValueAt(User_idum.getText(), i, 0);
                model.setValueAt(Usernameum.getText(), i, 1);
                model.setValueAt(Passwordum.getText(), i, 2);
                model.setValueAt(Phonenoum.getText(), i, 3);
                model.setValueAt(Emailum.getText(), i, 4);
                model.setValueAt(Combousermanagement.getSelectedItem().toString(), i, 5);
                JOptionPane.showMessageDialog(this, "Data has been Successfully Updated.");
            } else {
                JOptionPane.showMessageDialog(this, "Error While Updating.");
            }

        }

    }//GEN-LAST:event_Update_btnumActionPerformed

