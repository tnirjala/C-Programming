    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        managingItems hi = new managingItems();
        managingItemsdao daoms = new managingItemsdaoimpl();

        hi.setSn(sn1.getText());
        hi.setItems(item1.getText());
        hi.setPrice(Integer.parseInt(price1.getText()));


        if (sn1.getText().equals("") || sn1.getText().equals("") || item1.getText().equals("")  || price1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Fill all the field to process.");
        } else {
            daoms.Updatebtnum(hi);

            int i = data_table.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) data_table.getModel();
            if (i >= 0) {
                model.setValueAt(sn1.getText(), i, 0);
                model.setValueAt(item1.getText(), i, 1);
                model.setValueAt(price1.getText(), i, 2);
                JOptionPane.showMessageDialog(this, "Data has been Successfully Updated.");
            } else {
                JOptionPane.showMessageDialog(this, "Error While Updating.");
            }

        }
    }//GEN-LAST:event_updateBtnActionPerformed

