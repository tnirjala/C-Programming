    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        String record, item;
        managingItems hi = new managingItems();
        hi.setItems(item1.getText());

        File db = new File("Menu.txt");
        File tempdb = new File("Menutmp.txt");

        BufferedReader br;
        try {
            br = new BufferedReader(new FileReader(db));
            BufferedWriter bw = new BufferedWriter(new FileWriter(tempdb));

            item= hi.getItems();

            while ((record = br.readLine()) != null) {
                if (record.contains(item)) {
                    continue;
                }
                bw.write(record);
                bw.flush();
                bw.newLine();
            }
            br.close();
            bw.close();
            db.delete();

            tempdb.renameTo(db);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Usermanagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Usermanagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel tbl = (DefaultTableModel) data_table.getModel();
        if (data_table.getSelectedRowCount() == 1) {
            tbl.removeRow(data_table.getSelectedRow());
            JOptionPane.showMessageDialog(this, "The Data Has been Deleted Sucessfully.");

        } else {
            if (data_table.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Empty Table Found");
            } else {
                JOptionPane.showMessageDialog(this, "Select the Row to Delete the record");
            }
        }
    }

